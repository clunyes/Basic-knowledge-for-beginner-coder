package android.组件化;

public class 组件化 {
    /**
     * 组件化开发就是将一个app分成多个模块，每个模块都是一个组件（Module），开发的过程中我们可以让这些组件相互依赖或者单独调试部分组件等，
     * 但是最终发布的时候是将这些组件合并统一成一个apk，这就是组件化开发。
     *
     * 1.将APP业务模块分开开发和维护，有利于提高效率
     * 2.一个业务分为一个组件，每个组件可以单独开发以实现并发开发，进一步提高效率
     * 3.每个组件充分解耦
     * 4.每个组件可以单独测试
     * 5.架构灵活
     */
}
