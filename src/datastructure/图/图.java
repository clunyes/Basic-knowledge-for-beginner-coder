package datastructure.图;

public class 图 {
    /**
     * 牛逼格拉斯的图，面试面到崩溃，就是这货
     *
     * 在计算机科学中，一个图就是一些顶点的集合，这些顶点通过一系列边结对（连接）。
     * 顶点用圆圈表示，边就是这些圆圈之间的连线。顶点之间通过边连接。
     *
     * 1.我们这一次介绍了图的定义和分类。根据图的边是否有方向，可分为有向图和无向图。
     * 根据图的边是否有权重，可分为带权图和无权图。当然，也可以把两个维度结合起来描述，比如有向带权图，无向无权图等等。
     *
     * 2.图的表示方法有很多种。包括邻接矩阵、邻接表、逆邻接表、十字链表（还有一种邻接多重表，有兴趣的小伙伴可以自学下）。
     */
    /**
     * java中，用什么来表示图呢。。。
     * 从数据的表示方法来说，有二种表示图的方式：一种是邻接矩阵，其实是一个二维数组；
     * 一种是邻接表，其实是一个顶点表，每个顶点又拥有一个边列表。
     */
}
